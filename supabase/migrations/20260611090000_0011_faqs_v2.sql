-- CMP-DIRECT 자주묻는질문(FAQ) v2 — 다중 카테고리 + 정수 id + 시트 정본 44문항 재시드.
--
-- v1(0010_faqs.sql)에서 달라지는 점:
--   1. ``id``: uuid → bigint generated always as identity. 상세 페이지 URL
--      (``/faq/{faqId}``)에 쓰는 사람이 읽을 수 있는 식별자.
--   2. ``category text`` → ``categories text[]``: 한 질문이 여러 카테고리(예: 비용 +
--      행위허가 + 사용검사)에 동시에 속할 수 있다.
--   3. 시드 전면 교체: 운영자 검수를 마친 [집핀] QnA 시트(2026-06-11) 44문항.
--
-- FAQ 는 공개 콘텐츠(PII 아님)이고 다른 테이블이 참조하지 않으므로 alter 대신
-- drop-recreate 로 간다(콘텐츠 정본은 아래 시드). 읽기 경로 정책은 v1 과 동일:
-- FastAPI ``GET /faqs`` 가 권한 role 로 RLS 를 우회 SELECT 하며 anon/authenticated 에
-- 어떤 grant/policy 도 부여하지 않는다([api].enabled=false 로 PostgREST 도 차단).
--
-- ``answer`` 는 마크다운 텍스트(표·링크·인라인 HTML 일부 포함 가능). 렌더링은
-- 프론트(`/faq`, `/faq/[faqId]`)가 담당한다.

drop table if exists public.faqs;

create table public.faqs (
  id bigint generated always as identity,
  categories text[] not null,
  question text not null,
  answer text not null,
  sort_order integer not null default 0,
  is_published boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint pk_faqs primary key (id),
  -- 허용 슬러그 외 값과 빈 배열을 차단한다(``<@`` 는 빈 배열에 true 이므로 길이 검사 병행).
  constraint ck_faqs_categories_allowed check (
    categories <@ array[
      'cost', 'prereview', 'glossary', 'act_permit',
      'resident_consent', 'fireproofing', 'use_inspection'
    ]::text[]
    and array_length(categories, 1) >= 1
  )
);

-- 공개 목록 조회용 부분 인덱스 — 노출 행을 전역 정렬(sort_order) 순으로 읽는 단일 경로.
-- 카테고리 필터·검색·페이징은 프론트가 전체 목록(현재 44건)을 받아 클라이언트에서
-- 처리하므로 categories GIN 인덱스는 두지 않는다(서버 필터 도입 시 추가).
create index ix_faqs_published_sort_order
  on public.faqs (sort_order)
  where is_published;

comment on table public.faqs is
  'Public FAQ content (not PII). Read via the FastAPI GET /faqs path; no PostgREST/client grants. answer holds markdown; categories is an array of English slugs mapped to Korean labels by the web app.';

